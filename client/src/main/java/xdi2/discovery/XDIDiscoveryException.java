package xdi2.discovery;

public class XDIDiscoveryException extends Exception {

	private static final long serialVersionUID = 4500240992794516180L;

	public XDIDiscoveryException() {

		super();
	}

	public XDIDiscoveryException(String message) {

		super(message);
	}

	public XDIDiscoveryException(Throwable cause) {

		super(cause);
	}

	public XDIDiscoveryException(String message, Throwable cause) {

		super(message, cause);
	}
}
